    server_log "üì¶ Instalando PM2..."
    npm install -g pm2
fi

if ! command -v nginx >/dev/null 2>&1; then
    server_log "üì¶ Instalando Nginx..."
    apt-get update && apt-get install -y nginx
fi

# Backup da vers√£o atual se existir
