info() { echo -e "${PURPLE}[INFO] $1${NC}" | tee -a "$LOG_FILE"; }

echo "üöÄ ULTRAZEND - ROBUST DEPLOYMENT"
echo "================================="
log "üéØ Deploy ID: $DEPLOY_ID"
log "üåê Servidor: $SERVER_HOST"
log "üìÅ Deploy Path: $DEPLOY_PATH"
log "üìä Log File: $LOG_FILE"
echo ""

# 1. PRE-DEPLOYMENT VALIDATIONS
log "PASSO 1: Valida√ß√µes robustas pr√©-deploy..."

# Verificar arquivos essenciais localmente
[ ! -f "backend/package.json" ] && error "backend/package.json n√£o encontrado"
[ ! -f "backend/src/index.ts" ] && error "backend/src/index.ts n√£o encontrado" 
[ ! -f "ecosystem.config.js" ] && error "ecosystem.config.js n√£o encontrado"

