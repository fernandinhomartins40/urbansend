if [ "$MIGRATION_SUCCESS" = "false" ]; then
    server_log "‚ö†Ô∏è Migra√ß√µes falharam ap√≥s $MIGRATION_RETRIES tentativas - continuando"
fi

# OTIMIZAR PARA PRODU√á√ÉO
server_log "üßπ Otimizando para produ√ß√£o..."
npm prune --omit=dev

cd ..
server_log "‚úÖ Configura√ß√£o no servidor conclu√≠da"
