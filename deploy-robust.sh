    server_log "üíæ Criando backup da vers√£o atual..."
    BACKUP_DIR="/var/backups/ultrazend/backup-$(date +%Y%m%d-%H%M%S)"
    mkdir -p "$BACKUP_DIR"
    
    # Parar aplica√ß√£o antes do backup
    pm2 stop ultrazend 2>/dev/null || server_log "Nenhuma aplica√ß√£o PM2 para parar"
    
    # Fazer backup completo
    cp -r /var/www/ultrazend/backend "$BACKUP_DIR/" 2>/dev/null || true
    cp -r /var/www/ultrazend/frontend "$BACKUP_DIR/" 2>/dev/null || true
    cp /var/www/ultrazend/ecosystem.config.js "$BACKUP_DIR/" 2>/dev/null || true
    cp /var/www/ultrazend/backend/.env "$BACKUP_DIR/" 2>/dev/null || true
    
    server_log "‚úÖ Backup criado em: $BACKUP_DIR"
    
    # Limpar instala√ß√£o anterior
    server_log "üßπ Limpando instala√ß√£o anterior..."
    rm -rf /var/www/ultrazend/backend/node_modules || true
    rm -rf /var/www/ultrazend/frontend/node_modules || true
    rm -rf /var/www/ultrazend/backend/dist || true
fi

server_log "‚úÖ Servidor preparado"
EOFSERVER

success "Servidor preparado e backup criado"

# 4. CODE TRANSFER WITH ROBUST HANDLING
log "PASSO 4: Transferindo c√≥digo com handling robusto..."

# Build local do backend primeiro
log "üèóÔ∏è Building backend localmente..."
cd backend
npm ci
npm run build
cd ..

# Verificar se build foi bem-sucedido
[ ! -f "backend/dist/index.js" ] && error "Build do backend falhou - dist/index.js n√£o encontrado"

# Transfer arquivos com rsync robusto
log "üì§ Transferindo arquivos para servidor..."
rsync -avz --progress \
    --exclude='node_modules/' \
    --exclude='*.log' \
    --exclude='.git/' \
    --exclude='coverage/' \
    --exclude='__tests__/' \
    -e "ssh -o StrictHostKeyChecking=no" \
    ./ $SERVER_USER@$SERVER_HOST:$DEPLOY_PATH/

success "C√≥digo transferido com sucesso"

# 5. ROBUST SERVER SETUP
log "PASSO 5: Configura√ß√£o robusta no servidor..."

ssh $SERVER_USER@$SERVER_HOST << 'EOFSETUP'
cd /var/www/ultrazend
server_log() { echo "[SERVER] $1"; }

server_log "üîß Iniciando configura√ß√£o robusta..."

# CONFIGURA√á√ÉO .ENV COM FALLBACK ROBUSTO (implementa nossas corre√ß√µes)
server_log "‚öôÔ∏è Configurando .env com fallback robusto..."
ENV_CONFIGURED=false

# 1. Tentar .env.production.deploy (nosso arquivo especial)
if [ -f "backend/.env.production.deploy" ]; then
    server_log "‚úÖ Usando backend/.env.production.deploy"
    cp backend/.env.production.deploy backend/.env
    ENV_CONFIGURED=true
# 2. Tentar configs/.env.production  
elif [ -f "configs/.env.production" ]; then
    server_log "‚úÖ Usando configs/.env.production"
    cp configs/.env.production backend/.env
    ENV_CONFIGURED=true
# 3. Tentar backend/.env.production
elif [ -f "backend/.env.production" ]; then
    server_log "‚úÖ Usando backend/.env.production"
    cp backend/.env.production backend/.env
    ENV_CONFIGURED=true
# 4. Verificar se j√° existe
elif [ -f "backend/.env" ]; then
    server_log "‚úÖ backend/.env j√° presente"
    ENV_CONFIGURED=true
fi

if [ "$ENV_CONFIGURED" = "false" ]; then
    server_log "‚ùå Nenhum arquivo .env encontrado!"
    find . -name ".env*" -type f | head -5
    exit 1
fi

chmod 600 backend/.env
chown www-data:www-data backend/.env 2>/dev/null || chown root:root backend/.env
server_log "‚úÖ Configura√ß√£o .env conclu√≠da com fallback robusto"

# INSTALAR DEPEND√äNCIAS NO SERVIDOR
server_log "üì¶ Instalando depend√™ncias no servidor..."
cd backend

# Limpeza completa
rm -rf node_modules package-lock.json || true
npm cache clean --force

# Instalar TODAS as depend√™ncias (incluindo dev para build)
if ! npm ci; then
    server_log "‚ùå Falha ao instalar depend√™ncias"
    exit 1
fi

# VERIFICAR BUILD OU CRIAR SE NECESS√ÅRIO
if [ ! -f "dist/index.js" ]; then
    server_log "üèóÔ∏è Build n√£o encontrado, executando build no servidor..."
    if ! npm run build; then
        server_log "‚ùå Build falhou no servidor"
        exit 1
    fi
fi

# Verificar se build foi bem-sucedido
if [ ! -f "dist/index.js" ]; then
    server_log "‚ùå Build falhou - dist/index.js n√£o foi gerado"
    exit 1
fi

server_log "‚úÖ Build verificado com sucesso"

# EXECUTAR MIGRA√á√ïES COM RETRY (implementa nossas corre√ß√µes)
server_log "üìä Executando migra√ß√µes com retry..."
MIGRATION_RETRIES=3
MIGRATION_SUCCESS=false

for attempt in $(seq 1 $MIGRATION_RETRIES); do
    server_log "Tentativa de migra√ß√£o $attempt/$MIGRATION_RETRIES..."
    if npm run migrate:latest; then
        MIGRATION_SUCCESS=true
        break
    else
        server_log "‚ö†Ô∏è Falha na migra√ß√£o (tentativa $attempt)"
        if [ $attempt -lt $MIGRATION_RETRIES ]; then
            sleep 5
        fi
    fi
