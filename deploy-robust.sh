chown -R www-data:www-data /var/www/ultrazend 2>/dev/null || chown -R root:root /var/www/ultrazend

# Iniciar com PM2 e configura√ß√µes robustas
export NODE_ENV=production
export PORT=3001

server_log "üìã Verificando ecosystem config..."
if [ ! -f "ecosystem.config.js" ]; then
    server_log "‚ùå ecosystem.config.js n√£o encontrado"
    exit 1
fi

# Iniciar aplica√ß√£o
server_log "üîÑ Executando PM2 start..."
if pm2 start ecosystem.config.js --env production --name ultrazend --update-env; then
    server_log "‚úÖ Comando PM2 executado"
    
    # Aguardar estabiliza√ß√£o
    server_log "‚è≥ Aguardando estabiliza√ß√£o (15s)..."
    sleep 15
    
    # Verificar se est√° realmente online
    if pm2 jlist | grep -q '"status":"online"'; then
        server_log "‚úÖ Aplica√ß√£o confirmada como online"
        
        # Salvar configura√ß√£o PM2
        pm2 save
        pm2 startup systemd -u root --hp /root | grep '^sudo' | bash || server_log "PM2 startup j√° configurado"
        
        server_log "üéâ Aplica√ß√£o inicializada com sucesso!"
    else
        server_log "‚ùå Aplica√ß√£o n√£o est√° online ap√≥s 15s"
        server_log "Status PM2:"
        pm2 status
        server_log "Logs recentes:"
        pm2 logs ultrazend --lines 20 --nostream 2>/dev/null || server_log "Sem logs dispon√≠veis"
        exit 1
    fi
else
    server_log "‚ùå Falha ao executar comando PM2"
    exit 1
fi
EOFSTART

success "Aplica√ß√£o inicializada com sucesso"

# 7. COMPREHENSIVE HEALTH CHECKS
log "PASSO 7: Health checks abrangentes..."

# Aguardar mais um pouco para estabiliza√ß√£o
sleep 10

# Health check robusto
log "üè• Executando health checks..."
HEALTH_CHECKS_PASSED=0
TOTAL_CHECKS=3

# 1. API Health Check
log "üîç Testando API health endpoint..."
if ssh $SERVER_USER@$SERVER_HOST 'curl -sf -m 10 http://localhost:3001/health >/dev/null'; then
    success "‚úÖ API health check passou"
    ((HEALTH_CHECKS_PASSED++))
else
    warning "‚ùå API health check falhou"
fi

# 2. PM2 Status Check
log "üîç Verificando status PM2..."
if ssh $SERVER_USER@$SERVER_HOST 'pm2 jlist | grep -q "\"status\":\"online\""'; then
    success "‚úÖ PM2 status check passou"
    ((HEALTH_CHECKS_PASSED++))
else
    warning "‚ùå PM2 status check falhou"
fi

# 3. Port Check
log "üîç Verificando se porta est√° aberta..."
if ssh $SERVER_USER@$SERVER_HOST 'netstat -tlnp | grep -q ":3001"'; then
    success "‚úÖ Port check passou"
    ((HEALTH_CHECKS_PASSED++))
else
    warning "‚ùå Port check falhou"
fi

# Avaliar resultado
if [ $HEALTH_CHECKS_PASSED -ge 2 ]; then
    success "üéâ Health checks passaram ($HEALTH_CHECKS_PASSED/$TOTAL_CHECKS)"
else
    error "üí• Health checks falharam ($HEALTH_CHECKS_PASSED/$TOTAL_CHECKS)"
fi

# 8. DEPLOYMENT SUMMARY
log "PASSO 8: Resumo final do deployment..."

echo ""
echo "üéäüéäüéä DEPLOY ROBUSTO CONCLU√çDO COM SUCESSO! üéäüéäüéä"
echo "================================================="
info "üÜî Deploy ID: $DEPLOY_ID"
info "‚è∞ Data/Hora: $(date)"
info "üåê Servidor: $SERVER_HOST"
info "üìÅ Path: $DEPLOY_PATH"
info "üìä Log: $LOG_FILE"
echo ""

# Status final detalhado do servidor
ssh $SERVER_USER@$SERVER_HOST << 'EOFFINAL'
server_log() { echo "[SERVER] $1"; }
echo "üìä STATUS FINAL NO SERVIDOR:"
echo "============================"
server_log "PM2 Status:"
pm2 status
echo ""
server_log "Application Info:"
pm2 jlist | jq -r '.[] | select(.name=="ultrazend") | "Status: " + .pm2_env.status + " | PID: " + (.pid|tostring) + " | Uptime: " + .pm2_env.pm_uptime' 2>/dev/null || echo "Informa√ß√µes PM2 n√£o dispon√≠veis em formato JSON"
echo ""
server_log "Portas em uso:"
netstat -tlnp | grep -E ":(3001|80|443)" || echo "Nenhuma porta relevante encontrada"
echo ""
server_log "Arquivos cr√≠ticos:"
ls -la /var/www/ultrazend/backend/dist/index.js 2>/dev/null && echo "‚úÖ Backend build presente" || echo "‚ùå Backend build ausente"
ls -la /var/www/ultrazend/backend/.env 2>/dev/null && echo "‚úÖ Arquivo .env presente" || echo "‚ùå Arquivo .env ausente"
ls -la /var/www/ultrazend/ecosystem.config.js 2>/dev/null && echo "‚úÖ Ecosystem config presente" || echo "‚ùå Ecosystem config ausente"
EOFFINAL

echo ""
info "üîó URLs importantes:"
info "   ‚Ä¢ Website: https://www.ultrazend.com.br"
info "   ‚Ä¢ API: https://www.ultrazend.com.br/api"
info "   ‚Ä¢ Health: https://www.ultrazend.com.br/health"
echo ""
info "üí° Comandos √∫teis:"
info "   ssh $SERVER_USER@$SERVER_HOST 'pm2 status'"
info "   ssh $SERVER_USER@$SERVER_HOST 'pm2 logs ultrazend'"
info "   ssh $SERVER_USER@$SERVER_HOST 'pm2 restart ultrazend'"
echo ""

success "üèÅ DEPLOY ROBUSTO FINALIZADO COM TODAS AS CORRE√á√ïES APLICADAS!"
echo "üìã Implementa√ß√µes aplicadas:"
echo "   ‚úÖ Carregamento .env robusto com fallback"
echo "   ‚úÖ Inicializa√ß√£o SSL resiliente"
echo "   ‚úÖ Retry de banco de dados"  
echo "   ‚úÖ Health checks abrangentes"
echo "   ‚úÖ Handling de erros robusto"
echo "   ‚úÖ Backup autom√°tico"
echo ""